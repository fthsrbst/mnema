export type IconName = keyof typeof ICONS;

export function Icon({ name, size = 16, className }: { name: IconName; size?: number; className?: string }) {
  const bitmap = ICONS[name];
  const n = bitmap.length;
  return (
    <svg width={size} height={size} viewBox={`0 0 ${n} ${n}`} className={className} aria-hidden="true" shapeRendering="crispEdges">
      {bitmap.map((row, y) =>
        row
          .split("")
          .map((cell, x) => (cell === "1" ? <rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill="currentColor" /> : null))
      )}
    </svg>
  );
}
